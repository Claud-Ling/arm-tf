//set BGD
void
setBGD(unsigned int x0,
       unsigned int x1,
       unsigned int y0,
       unsigned int y1,
       unsigned int addr,
       unsigned int rowbyte,
       unsigned int xscale, unsigned int yscale, unsigned int alpha)
{
//hstart
	bootload_SetRegB1(0x15021a40, x0 & 0xff);
	bootload_SetRegB1(0x15021a41, (x0 >> 8) & 0xff);
//hend
	bootload_SetRegB1(0x15021a42, x1 & 0xff);
	bootload_SetRegB1(0x15021a43, (x1 >> 8) & 0xff);
//vstart
	bootload_SetRegB1(0x15021a44, y0 & 0xff);
	bootload_SetRegB1(0x15021a45, (y0 >> 8) & 0xff);
//vend
	bootload_SetRegB1(0x15021a46, y1 & 0xff);
	bootload_SetRegB1(0x15021a47, (y1 >> 8) & 0xff);
//starting address
	addr >>= 4;
	bootload_SetRegB1(0x15021a48, addr & 0xff);
	bootload_SetRegB1(0x15021a49, (addr >> 8) & 0xff);
	bootload_SetRegB1(0x15021002, ((addr >> 21) & 0x7) );
	bootload_SetRegB1(0x15021a4a, ((addr >> 16) & 0x1f) | 0x40);
//rowbyte
	bootload_SetRegB1(0x15021a4c, rowbyte & 0xff);
	bootload_SetRegB1(0x15021a4d, (rowbyte >> 8) & 0xff);
//h scale
	bootload_SetRegB1(0x15021a50, xscale & 0xff);
	bootload_SetRegB1(0x15021a51, (xscale >> 8) & 0xff);
//v scale
	bootload_SetRegB1(0x15021a52, yscale & 0xff);
	bootload_SetRegB1(0x15021a53, (yscale >> 8) & 0xff);
//8 bpp
	bootload_SetRegB1(0x15021a54, 0x2);
//alpha
	bootload_SetRegB1(0x15021a55, alpha & 0x0f);
//turn on BGD
	bootload_SetRegB1(0x15021936, 0x0);
	bootload_SetRegB1(0x15021931, 0x8);
}

