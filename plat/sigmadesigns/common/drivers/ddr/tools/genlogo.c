//the file format is:
//    BPP      - 2 bytes
//    Width    - 2 bytes
//    height   - 2 bytes
//    stride   - 2 bytes
//    MAXIndex - 2 bytes, if MAXIndex == 0xffff, then no "palettes"
//    palettes - (4 * MAXIndex) bytes, ARGB format
//    bitmap   - (stride * height) bytes
//
void
loadDDB(char *ddbname,char *imgname, unsigned int hstart, unsigned int vstart, unsigned int addr)
{				//loadDDB, just for debug
	int i;
	unsigned int gfxbpp, width, height, stride, maxIndex;
	unsigned char *ch;
	unsigned int sh=0;
	FILE *ddbfile;
	FILE *imgfile;


	ddbfile=fopen(ddbname,"rb");
	if(ddbfile==NULL){
		PRINT("error! %s:%d\n",__FILE__,__LINE__);
		return;
	}

	ch = (unsigned char *) &sh;

	fread(&ch[0],1,1,ddbfile);
	fread(&ch[1],1,1,ddbfile);
	PRINT("gfxbpp is %d\n",sh);
	gfxbpp = sh;

	fread(&ch[0],1,1,ddbfile);
	fread(&ch[1],1,1,ddbfile);
	PRINT("width is %d\n",sh);
	width = sh;

	fread(&ch[0],1,1,ddbfile);
	fread(&ch[1],1,1,ddbfile);
	PRINT("height is %d\n",sh);
	height = sh;

	fread(&ch[0],1,1,ddbfile);
	fread(&ch[1],1,1,ddbfile);
	PRINT("stride is %d\n",sh);
	stride = sh;

	fread(&ch[0],1,1,ddbfile);;
	fread(&ch[1],1,1,ddbfile);;
	PRINT("maxIndex is %d\n",sh);
	maxIndex = sh;

	InitCRTC();
	if (maxIndex != 0xffff) {
		for (i = 0; i < maxIndex; i++) {
			fread(&ch[0],1,1,ddbfile);
			fread(&ch[1],1,1,ddbfile);
			fread(&ch[2],1,1,ddbfile);
			fread(&ch[3],1,1,ddbfile);
			
			setPallette0(i, ch[0], ch[1], ch[2], ch[3]);
			
			PRINT("%d: %02x %02x %02x %02x\n",i,ch[0],ch[1],ch[2],ch[3]);
		}
	}
	setBGD(hstart, hstart + width, vstart, vstart + height, addr,
		 stride, 0x400, 0x400, 0x0f);
	imgfile=fopen(imgname,"wb");
	if(imgfile==NULL){
		PRINT("error! %s:%d\n",__FILE__,__LINE__);
		return;
	}

	for (i = 0; i < height * stride; i++) {
		fread(&sh,1,1,ddbfile);
		fwrite(&sh,1,1,imgfile);
	}
	fclose(imgfile);
	fclose(ddbfile);
}

